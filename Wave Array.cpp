/******************************************************************************************************************************************
Sort the given array and swap the adjacent elements.
******************************************************************************************************************************************/
vector<int> Solution::wave(vector<int> &A) {
    int n = A.size();
    sort(A.begin(),A.end());
    
    int i=0;
    while(i<n-1) {
        swap(A[i], A[i+1]);
        i+=2;
        
    }
    return A;
}
